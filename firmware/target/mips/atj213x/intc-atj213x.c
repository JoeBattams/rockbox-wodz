#include "config.h"
#include "system.h"
#include "intc-atj213x.h"
#include "atj213x.h"

void atj213x_intc_unmask(unsigned int)
{
    INTC_CFG2 |= (1<<int);
    INTC_MSK |= (1<<int);
}

void atj213x_intc_mask(unsigned int)
{
    INTC_MSK &= ~(1<<int);
}
